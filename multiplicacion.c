#include <stdio.h>

int main(){
		
	int num1, num2, producto;
		
	printf("Ingresa los numeros que deseas multiplicar: \n");
	scanf("%d %d", &num1, &num2);
	
	producto = num1 * num2;
	
	if(num1, num2 > 10)
		
		printf("El resultado de la multiplicacion es: %d", producto);

	else
		
		printf("Los numeros no son mayores a 10");
	
	return 0;
}
